// js - Big Int

// decimal format
let bigNum = BigInt(
    "123422222222222222222222222222222222222");
console.log(bigNum);

// hexadecimal format
let bigHex = BigInt("0x1ffffffeeeeeeeeef");
console.log(bigHex);

// binary format
let bigBin = BigInt(
    "0b1010101001010101001111111111111111");
console.log(bigBin);